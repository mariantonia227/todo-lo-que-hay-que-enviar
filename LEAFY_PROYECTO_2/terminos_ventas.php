<h1>Términos y Condiciones de Ventas</h1>

<p>Estos términos regulan las transacciones realizadas dentro de la plataforma entre compradores y vendedores.</p>

<h2>Responsabilidad del vendedor</h2>
<p>El vendedor es responsable de la veracidad de la información del producto, incluyendo descripción, precio y estado.</p>

<h2>Responsabilidad del comprador</h2>
<p>El comprador debe revisar cuidadosamente la información del producto antes de realizar la compra.</p>

<h2>Precios</h2>
<p>Los precios son establecidos por cada vendedor y pueden variar según el producto.</p>

<h2>Pagos</h2>
<p>Los pagos se acuerdan directamente entre comprador y vendedor. La plataforma no gestiona pagos directamente.</p>

<h2>Envíos</h2>
<p>El envío del producto es responsabilidad del vendedor, salvo acuerdo diferente entre las partes.</p>

<h2>Cancelaciones</h2>
<p>Las cancelaciones deben acordarse entre comprador y vendedor.</p>

<h2>Productos usados</h2>
<p>Algunos productos pueden ser de segunda mano, por lo que pueden presentar señales de uso.</p>

<h2>Limitación de responsabilidad</h2>
<p>La plataforma actúa como intermediaria y no se hace responsable por conflictos entre usuarios.</p>

